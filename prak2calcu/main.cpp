#include <iostream>

using namespace std;

int main()
{
    int a,b,c;
    cout<<endl<<"Masukan Nilai a = ";
    cin>>a;
    cout<<"Masukan Nilai b = ";
    cin>>b;
    cout<<"Masukan Nilai c = ";
    cin>>c;
    cout<<"------------------------\n";
    cout<<a<<"+"<<b<<"=";
    cout<<a+b+c<<endl;
    cout<<a<<"-"<<b<<"=";
    cout<<a-b+c<<endl;
    cout<<a<<"*"<<b<<"=";
    cout<<a*b+c<<endl;
    cout<<a<<"/"<<b<<"=";
    cout<<a/b+c<<endl;
    cout<<a<<"%"<<b<<"=";
    cout<<a%b+c<<endl;

    return 0;
}
